public class Car extends Vehicle{
    private String model, brand;

    public Car(){

    }
    public void setModel(String model){
        this.model = model;
    }
    public String getModel() {
        return model;
    }

    public void setBrand(String brand){
        this.brand = brand;
    }
    public String getBrand() {
        return brand;
    }
}
